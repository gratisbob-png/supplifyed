async function connectDb(): Promise<Surreal> {
  const db = new Surreal();
  await db.connect(process.env.SURREALDB_URL ?? 'http://localhost:8000');
  await db.signin({
    username: process.env.SURREALDB_USER ?? 'root',
    password: process.env.SURREALDB_PASS ?? 'root',
  });
  await db.use({
    namespace: process.env.SURREALDB_NS ?? 'supplifyed',
    database: process.env.SURREALDB_DB ?? 'production',
  });
  return db;
}

async function seedEvidenceMissing() {
  console.log('Connecting to SurrealDB...');
  const db = await connectDb();

  console.log(`Seeding ${EVIDENCE.length} evidence nodes for missing ingredients...\n`);

  let ok = 0;
  let errors = 0;

  for (const seed of EVIDENCE) {
    const { ingredient_id, relevance, direction, id: seedId, ...evidenceFields } = seed;
    const [evTable, ...evIdParts] = seedId.split(':');
    const evRid = new RecordId(evTable, evIdParts.join(':'));
    const [ingTable, ...ingIdParts] = ingredient_id.split(':');
    const ingRid = new RecordId(ingTable, ingIdParts.join(':'));

    try {
      await db.upsert(evRid).content({ ...evidenceFields });
      await db.query(
        `RELATE $ing->supported_by->$ev SET relevance = $r, direction = $d`,
        { ing: ingRid, ev: evRid, r: relevance, d: direction }
      );
      console.log(`  ✓ [${direction.toUpperCase().padEnd(12)}] → ${ingredient_id.split(':')[1]}`);
      ok++;
    } catch (err) {
      console.error(`  ✗ ${seedId}: ${err}`);
      errors++;
    }
  }

  await db.close();
  console.log(`\n${ok} evidence nodes seeded · ${errors} errors`);
  if (errors > 0) process.exit(1);
}

seedEvidenceMissing().catch((err) => {
  console.error('Fatal:', err);
  process.exit(1);
});
